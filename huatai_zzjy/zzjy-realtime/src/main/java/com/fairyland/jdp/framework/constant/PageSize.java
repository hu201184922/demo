public enum PageSize {
    _5(5), _10(10), _20(20), _50(50), _MAX(Integer.MAX_VALUE);

    private int value;

    private PageSize(int value) {
	this.value = value;
    }

    public int getValue() {
	return value;
    }

    @Override
    public String toString() {
	return String.valueOf(value);
    }
}
