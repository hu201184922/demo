package com.fairyland.jdp.framework.uploadtemplate.domain;

import java.util.Date;

public class SpecialDateWish {
	 private long id;
	 
	 private String specialDateName;//特殊日名称
	 
	 private String type;//特殊日类型
	 
	 private String wishContent;//祝福内容
	 
	 private Date createtime;//创建时间

	 private Date modifytime;//修改时间
	 
	 private String createdBy;		//创建人
	 
	 private String modifiedBy;		//修改人
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getSpecialDateName() {
		return specialDateName;
	}

	public void setSpecialDateName(String specialDateName) {
		this.specialDateName = specialDateName;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getWishContent() {
		return wishContent;
	}

	public void setWishContent(String wishContent) {
		this.wishContent = wishContent;
	}

	public Date getCreatetime() {
		return createtime;
	}

	public void setCreatetime(Date createtime) {
		this.createtime = createtime;
	}

	public Date getModifytime() {
		return modifytime;
	}

	public void setModifytime(Date modifytime) {
		this.modifytime = modifytime;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public String getModifiedBy() {
		return modifiedBy;
	}

	public void setModifiedBy(String modifiedBy) {
		this.modifiedBy = modifiedBy;
	}
}
